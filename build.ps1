# Compilar el proyecto con pyinstaller mediante el archivo main.spec y el comando:
#
# ```powershell
pyinstaller main.spec
# ```
#
# El archivo main.spec es un archivo de configuración de pyinstaller que permite personalizar la compilación del proyecto. En este caso, se está indicando que se cree un ejecutable de consola, que se incluyan los archivos de la carpeta templates y que se excluyan los archivos de la carpeta env.
